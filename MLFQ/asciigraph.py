def printgantt(queue, time, *priorities):
    marks = ("#", "$", "@")
    for q in range(queue-1, -1, -1):
        print("=" * time)
        print("Q%s" % str(q))
        for i, prp in enumerate(priorities):
            for index in range(time):
                if prp[index] == q:
                    print(marks[i], end="")
                else:
                    print(" ", end="")
            print("")
    print("="*time)
    for t in range(time):
        if t % 50 == 0:
            print(t, end="")
        else:
            print(" ", end="")
    print(time)
    print("")

